public class Lesson1Brothers {
    public static void main(String[] args) {

        int z = (30 + 32 + 32 + 35)/3;
        int brother1 = z - 30;
        int brother2 = z - 32;
        int brother3 = z - 32;
        int brother4 = z - 35;

        System.out.println("Первому брату: " + brother1 + " лет \nВторому брату: " + brother2 + " лет \nТретьему брату: " + brother3 + " лет \nЧетвертому брату: " + brother4 + " лет");
    }

}
